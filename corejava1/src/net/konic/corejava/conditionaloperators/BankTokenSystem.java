package net.konic.corejava.conditionaloperators;

	public class BankTokenSystem {
	    public static void main(String[] args) {

	        int token = 100;   // starting token number

	        // Issue 5 tokens using ++
	        token++;   // 101
	        System.out.println("Token: " + token);

	        token++;   // 102
	        System.out.println("Token: " + token);

	        token++;   // 103
	        System.out.println("Token: " + token);

	        token++;   // 104
	        System.out.println("Token: " + token);

	        token++;   // 105
	        System.out.println("Token: " + token);
	    }
	}

